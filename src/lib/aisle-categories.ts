/** Best-effort keyword categorization for grouping the shopping list by aisle. */
export function categorizeItem(name: string): AisleCategory {
  const lower = name.toLowerCase();
  for (const category of AISLE_CATEGORIES) {
    if (category === "אחר") continue;
    const keywords = KEYWORDS[category];
    if (keywords.some((kw) => lower.includes(kw))) return category;
  }
  return "אחר";
}
